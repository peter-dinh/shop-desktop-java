    private void btn_product_1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_product_1ActionPerformed
        try {
            model = (DefaultTableModel) jtb_analytic_product.getModel();
            ResultSet rs = bll_analytic.Analytic_Type_Product();
            if(rs.first() == false)
            {
                JOptionPane.showMessageDialog(null, "Data is null");
            }else
            {
                if(model.getColumnCount() > 0)
                    model.setColumnCount(0);
                String header [] = {"ID Product", "Name Product", "Count"};
                for(String str : header)
                {
                    model.addColumn(str);
                }
                if(model.getRowCount() > 0)
                    model.setRowCount(0);
                int n =0;
                rs.last();
                n = rs.getRow();
                rs.first();
                for(int i=0; i < n; i++)
                {
                    model.addRow(new Object[] {rs.getString("id"),rs.getString("name"),rs.getInt("number")});
                    rs.next();
                }
            }
        } catch (SQLException ex) {
            ex.printStackTrace();
        }
        finally
        {
            con.Close_Connect();
        }
    }//GEN-LAST:event_btn_product_1ActionPerformed

