    private void btn_okActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_okActionPerformed
        if(action == 0)
        {
            try {
                DTO.district district = Get_Form();
                boolean x = bll.Add(district);
                if(x == true)
                {
                    this.setVisible(false);
                }
            } catch (SQLException ex) {
                ex.printStackTrace();
            }
        }
        if(action == 1)
        {
            int n = JOptionPane.showConfirmDialog(null,"You want *Edit* this item ?","Question",JOptionPane.OK_CANCEL_OPTION);
            if(n == JOptionPane.YES_OPTION)
            {
                try {
                    DTO.district district = Get_Form();
                    boolean x = bll.Update(district);
                    if(x == false)
                    {
                        JOptionPane.showMessageDialog(null, "Check data input again");
                        Info_District();
                    }
                    else
                        this.setVisible(false);
  
                } catch (SQLException ex) {
                    ex.printStackTrace();
                }
            }
        }
    }//GEN-LAST:event_btn_okActionPerformed

