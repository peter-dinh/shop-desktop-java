    BLL.bll_district bll = new BLL.bll_district();
    
    private DTO.district Get_Form() throws SQLException
    {
        DTO.district district = new DTO.district();
        
        if(action == 0)
        district.setId(bll.Get_Max_ID_District());
        else district.setId(this.id_district);
        district.setName_district(txt_district.getText());
        district.setName_city(txt_city.getText());
        return district;
    }
    
    
    private void Info_District()
    {
        DTO.district district = bll.Info_District(this.id_district);
        txt_district.setText(district.getName_district());
        txt_city.setText(district.getName_city());
    }
    
