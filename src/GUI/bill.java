    private String Name_Customer(String id)
    {
        if(id == null || id.equals("") == true) return null;
        else
        {
            DTO.customer kh = bll_customer.Info_Customer(id);
            return kh.getFirst_name()+ " " + kh.getLast_name();
        }
    }
    
    private String Name_Staff(String id)
    {
        if(id == null || id.equals("") == true) return null;
        else
        {
            DTO.staff nv = bll_staff.Info_Staff(id);
            return nv.getFirst_name()+" "+ nv.getLast_name();
        }
    }
    
    //Get name Payment
    private String Name_Pay(int pay)
    {
        if(pay == 0 ) return "Use Cash";
        return "Use Card";
    }
    //Add data in Jtable BIll
    private void AddRow(DTO.bill bill)
    {
        model = (DefaultTableModel) jtb_bill.getModel();
        model.addRow(new Object[] {bill.getId(), Name_Customer(bill.getId_customer()), Name_Staff(bill.getId_staff()),bill.getDatetime(), Name_Pay(bill.getPayment()), bill.getSale(), bill.getShell_out()});
    }
    
    
    
    //Load data in database to JTable Bill Info
    private void Load_Bill_Info()
    {
        model = (DefaultTableModel) jtb_bill_info.getModel();
        if(model.getRowCount() > 0)
            model.setRowCount(0);//trả về table rỗng
        // Nhap lieu vao table
        ArrayList<DTO.bill_info> List_bill_info = bll_info.List_Bill_Info(this.id_bill);
        for (DTO.bill_info bill : List_bill_info)
            AddRow_Bill_Info(bill);
        
        lb_sum_bill.setText(String.valueOf(bll_info.Sum_Bill(id_bill)));
        if(Integer.parseInt(lb_sale.getText()) ==  0)
            lb_shell_out.setText(String.valueOf(bll_info.Sum_Bill(id_bill)));
    }
    
    //Add data in Jtable Bill info
    private void AddRow_Bill_Info(DTO.bill_info bill)
    {
        model = (DefaultTableModel) jtb_bill_info.getModel();
        model.addRow(new Object[] {bill.getId_product(), bll_product.Get_Name_Product_By_ID(bill.getId_product()), bill.getPrice(), bill.getSale(), bill.getCount(), bill.getSum()});
    }
    
