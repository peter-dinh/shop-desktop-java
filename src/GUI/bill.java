    //Load data in JTable Bill
    private void Load()
    {
        
        //String header_vi[] = {""};
        //String header_en[] = {};        
        
        model = (DefaultTableModel) jtb_bill.getModel();
        if(model.getRowCount() > 0)
            model.setRowCount(0);//trả về table rỗng
        // Nhap lieu vao table
        ArrayList<DTO.bill> List_bill = bll.List_Bill();
        for (DTO.bill bill : List_bill)
            AddRow(bill);
    }
    
    
    
