    private void btn_delActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_delActionPerformed
        if(this.id_bill == 0)
        {
            JOptionPane.showMessageDialog(null, "You must select item bill");
        }
        else
        {
            int n = JOptionPane.showConfirmDialog(null,"You want *DELETE* this item ?","Question",JOptionPane.OK_CANCEL_OPTION);
            if(n == JOptionPane.YES_OPTION)
            {
                DTO.bill bill_info = bll.Info_Bill(this.id_bill);
                long sum = bill_info.getSum_bill();
                sum = -sum;
                bll_customer.Update_Accumulation(bill_info.getId_customer(), sum);
                bll_customer.Grownup_Rank(bill_info.getId_customer());
                ArrayList<DTO.bill_info> List_bill_info = bll_info.List_Bill_Info(this.id_bill);
                for (DTO.bill_info bill : List_bill_info)
                {
                    bll_product.Update_Count_Product(bill.getId_product(), 1, bill.getCount());
                }
                bll_info.Del_All_Bill_Product(this.id_bill);
                bll.Del(bll.Info_Bill(this.id_bill));
            }
            this.id_bill = 0;
            this.action = 0;
        }
    }//GEN-LAST:event_btn_delActionPerformed

