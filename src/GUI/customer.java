     private DTO.customer Get_Form()
    {
        DTO.customer customer = new DTO.customer();
        customer.setId_customer(txt_identity.getText());
        customer.setFirst_name(txt_first_name.getText());
        customer.setLast_name(txt_last_name.getText());
        customer.setAddress(txt_address.getText());
        customer.setId_district(Integer.parseInt(lb_district.getText().substring(0,2)));
        customer.setPhone(txt_phone.getText());
        customer.setDate_regiter(txt_date.getText());
        int xl =  10;
        customer.setId_rank(xl);
        return customer;
    }
    
     /*
        Hàm sử dụng để tìm kiếm các Customer thỏa mãn với từ khoa
     */
     
     
    private void Search()
    {
        model = (DefaultTableModel) jtb_customer.getModel();
        if(model.getRowCount() > 0)
            model.setRowCount(0);//trả về table rỗng
        if(txt_search.getText().equals("") == true)
            JOptionPane.showMessageDialog(null, "You must write \"Key Word\"");
        else
        {
            try {
                ArrayList <DTO.customer> List_Customer_Search = bll.List_Search((jcb_search.getSelectedIndex()+1),txt_search.getText());
                for( DTO.customer customer : List_Customer_Search)
                    AddRow(customer);
            } catch (SQLException ex) {
                ex.printStackTrace();
            }
        }
    }
     
    
