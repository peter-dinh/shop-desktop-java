    //Check Value before Add, Edit
    private boolean Check()
    {
        if(bll.Check_ID_Customer(txt_identity.getText()) == false)
        {
            JOptionPane.showMessageDialog(null, "IDentity is exsit in system");
            return false;
        }
        if(txt_identity.getText().length() < 9 || txt_identity.getText().length() > 10)
        {
            JOptionPane.showMessageDialog(null, "IDentity isn't invalid");
            return false;
        }
        if(txt_identity.getText().equals("") == true)
        {
            JOptionPane.showMessageDialog(null, "You don't text \"IDentity\" is null");
            return false;
        }
        if(txt_first_name.getText().equals("") == true)
        {
            JOptionPane.showMessageDialog(null, "You don't text \"First Name\" is null");
            return false;
        }
        if(txt_last_name.getText().equals("") == true)
        {
            JOptionPane.showMessageDialog(null, "You don't text \"Last Name\" is null");
            return false;
        }
        if(txt_address.getText().equals("") == true)
        {
            JOptionPane.showMessageDialog(null, "You don't text \"Address\" is null");
            return false;
        }
        if(lb_district.getText().equals("District") == true)
        {
            JOptionPane.showMessageDialog(null, "You must select district");
            return false;
        }
        if(txt_phone.getText().equals("") == true)
        {
            JOptionPane.showMessageDialog(null, "You don't text \"Phone\" is null");
            return false;
        }
        if(txt_date.getText().equals("") == true)
        {
            JOptionPane.showMessageDialog(null, "You don't text \"Date\" is null");
            return false;
        }
        return true;
    }
   
