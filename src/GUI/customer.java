    private void btn_okActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_okActionPerformed
        if(action == 2)
        {
            if(Check() == true) {
                DTO.customer customer = Get_Form();
                boolean x = bll.Add(customer);
                if(x == true)
                {
                    this.action=0;
                    Clear();
                    this.info_customer.setVisible(false);
                }
            }
        }
        if(action == 3)
        {
            this.txt_identity.setEditable(false);
            int n = JOptionPane.showConfirmDialog(null,"You want *Edit* this item ?","Question",JOptionPane.OK_CANCEL_OPTION);
            if(n == JOptionPane.YES_OPTION)
            {
                if(Check() == true) {
                    DTO.customer customer = Get_Form();
                    boolean x = bll.Update(customer);
                    if(x == false)
                    {
                        Info_Customer();
                    }
                    else
                    {
                        this.txt_identity.setEditable(true);
                        this.action=0;
                        Clear();
                        this.info_customer.setVisible(false);
                    }
                }
            }
        }
    }//GEN-LAST:event_btn_okActionPerformed

