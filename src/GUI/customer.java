    /*
        Hàm đưa giá trị của Khach Hang vào Jtable
    */
    
    private void AddRow(DTO.customer customer)
    {
        
        model = (DefaultTableModel) jtb_customer.getModel();
        model.addRow(new Object[] {customer.getId_customer(),customer.getFirst_name()+" "+customer.getLast_name(),customer.getAddress()});
    }
    
    
