package DTO;

/**
 *
 * @author Dinh Truong
 */
public class producer {
    private int id;
    private String name_producer;
    private String address;
    private String phone;
    private String mail;

    /**
     * @return the id
     */
    public int getId() {
        return id;
    }

    /**
     * @param id the id to set
     */
    public void setId(int id) {
        this.id = id;
    }

    /**
     * @return the name_producer
     */
    public String getName_producer() {
        return name_producer;
    }

    /**
     * @param name_producer the name_producer to set
     */
    public void setName_producer(String name_producer) {
        this.name_producer = name_producer;
    }

    /**
     * @return the address
     */
    public String getAddress() {
        return address;
    }

    /**
     * @param address the address to set
     */
    public void setAddress(String address) {
        this.address = address;
    }

    /**
     * @return the phone
     */
    public String getPhone() {
        return phone;
    }

    /**
     * @param phone the phone to set
     */
    public void setPhone(String phone) {
        this.phone = phone;
    }

    /**
     * @return the mail
     */
    public String getMail() {
        return mail;
    }

    /**
     * @param mail the mail to set
     */
    public void setMail(String mail) {
        this.mail = mail;
    }

}
